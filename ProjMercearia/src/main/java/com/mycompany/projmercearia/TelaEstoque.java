    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
       try {
        String nome = txtNome.getText().trim();
        double precoVenda = Double.parseDouble(txtPrecoVenda.getText().trim());
        int qtd = Integer.parseInt(txtQuantidade.getText().trim());
        
        String categoria = "Geral";
        double precoCompra = precoVenda * 0.7; 
        String fornecedor = "Padrão";

        
        Mercadoria mercadoria = new Mercadoria(0, nome, categoria, precoCompra, precoVenda, qtd, fornecedor);
        
        PainelControle controle = new PainelControle();
        controle.cadastrarMercadoria(mercadoria);
        
        javax.swing.JOptionPane.showMessageDialog(this, "Produto cadastrado e gerado automaticamente pelo banco!");
        
       
        txtNome.setText(""); 
        txtPrecoVenda.setText(""); 
        txtQuantidade.setText("");
        txtNome.requestFocus();
        
        } catch(Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro: Verifique os campos informados.");
        }
        carregarTabelaEstoque();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linhaSelecionada = tabelaEstoque.getSelectedRow();
    
        if (linhaSelecionada >= 0) {
          
            int codigo = Integer.parseInt(tabelaEstoque.getValueAt(linhaSelecionada, 0).toString());

            
            PainelControle controle = new PainelControle();
            controle.excluirMercadoria(codigo);

          
            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tabelaEstoque.getModel();
            modelo.removeRow(linhaSelecionada);

            javax.swing.JOptionPane.showMessageDialog(this, "Mercadoria excluída com sucesso do Banco de Dados!");
            carregarTabelaEstoque();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione uma linha na tabela para excluir.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

