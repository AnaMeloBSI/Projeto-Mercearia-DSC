    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String codigoStr = txtCodigo.getText().trim();
    
        if (codigoStr.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Digite um código para buscar!");
            return;
        }

        try {
            int codigoProcurado = Integer.parseInt(codigoStr);

            
            PainelControle controle = new PainelControle();
            Mercadoria m = controle.buscarMercadoriaPorCodigo(codigoProcurado);

         
            if (m != null) {
                txtNome.setText(m.getNome());
                txtPrecoVenda.setText(String.valueOf(m.getPrecoVenda()));
                txtQuantidade.setText(String.valueOf(m.getQuantidadeEstoque()));

                javax.swing.JOptionPane.showMessageDialog(this, "Produto encontrado! Altere os campos que desejar e clique em Salvar.");
            } else {
               
                javax.swing.JOptionPane.showMessageDialog(this, "Código não encontrado no banco. Preencha os campos para cadastrar como nova mercadoria!");
                txtNome.setText("");
                txtPrecoVenda.setText("");
                txtQuantidade.setText("");
                txtNome.requestFocus();
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro: O código digitado deve conter apenas números.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

